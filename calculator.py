def add(x,y):
    x,y=float(x),float(y)
    print(f"Sum : [{x+y}]")
def subtract(x,y):
    x,y=float(x),float(y)
    print(f"Difference : [{x-y}]")
def multiply(x,y):
    x,y=float(x),float(y)
    print(f"Product : [{x*y}]")
def division(x,y):
    x,y=float(x),float(y)
    print(f"Quotient : [{x//y}]\tRemainder : [{x%y}]")